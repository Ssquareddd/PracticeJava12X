package ex_09_Switch_statement;

public class Lab097_Interview {
    public static void main(String[] args) {


        char code = 'C';
        switch (code) {
            default -> System.out.println("Invalid code");
            case 'A' -> System.out.println("A");
            case 'B' -> System.out.println("B");
        }







    }
}
