package ex_03_Literals;

public class Lab029_Literals_P1 {
    public static void main(String[] args) {

        int age = 65;
        System.out.println(age);
        // Literal - Integral

        final float PI = 3.14f;
        System.out.println(PI);
        // literal - float

        final int dead_girl = 18;
        System.out.println(dead_girl);
        // here i have used literal - Integral have set constant value to it by adding final
    }
}
