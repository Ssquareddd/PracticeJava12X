package ex_12DoWhile;

public class Lab126_Diff_while_Dowhile {
    public static void main(String[] args) {


        int a = 0;
        while (a < 10) {
            System.out.println(a);
            a++;
        }

        do{
            System.out.println(a);
            a++;

        }while(a < 10);
    }
}
