package ex_04_Operators;

public class Lab037_Opearators_Arithmetic_Opearators {
    public static void main(String[] args) {

        /*
        Arithmetic Operators
        + (Addition)
        - (Subtraction )
        * (Multiplication)
        / (Division)
        % (Modules) -> it gives the remainder
         */
        int a = 20;
        int b = 45;
        float c  = 3.09f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a/c);
    }
}
