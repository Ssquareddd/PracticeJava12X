package ex_10_for_loop;

public class Lab110_Odd_even_from_top50 {
    public static void main(String[] args) {

        // To find the even numbers from 1 to 50
        // even -> i%2 == 0

        for (int i = 1; i <= 50; i++){
            if(i%2 ==0){
                System.out.println("Even -> " +i);
            }
        }





    }
}
